#학번: _____________  주제: 돼지들의 저녁 메뉴 중 삼겹살을 제거하라(동족상잔의 비극방지)
#문제: 반복문과 remove 메소드를 이용해 메뉴에서 "삼겹살"을 모두 제거하시오 

menu = ['삼겹살', '떡볶이', '덮밥']  # 메뉴 리스트 생성
#1. 저녁 메뉴 3개를 추가하고 입력한 음식명을 menu에 추가 후 결과 출력하기 
#반드시 while()문을 이용해서 수행할 것
i=1
while i<=3:
    food = input("음식명%d==>"%i)
    menu.append(food)    
    i +=1

#2. 메뉴 개수 출력
print("메뉴 개수= %d"%len(menu))

#3. 동족인 삼겹살은 모두 삭제하고 각각의 조건에 맞는 결과 출력하기
#반드시 for문과 복합자료형을 이용해서 수행할 것

# 방법 1: 'while'과 조건식 'item in menu'를 이용한 방법
# 동작 방식: '삼겹살'이 리스트 menu 안에 있는 동안 반복적으로 확인하여 제거

menu = ['삼겹살', '떡볶이', '덮밥', '삼겹살', '삼겹살', '삼겹살']
while '삼겹살' in menu:  # 리스트에 '삼겹살'이 있는 동안 루프 실행
    menu.remove('삼겹살')  # 리스트에서 첫 번째로 발견된 '삼겹살'을 제거

# 리스트의 각 반복마다 '삼겹살'이 있는지 확인하고, 한 번에 하나씩 제거
# 조건이 더 이상 만족하지 않으면 루프 종료

# 방법 2: 'while' 루프와 인덱스를 직접 제어하여 리스트 수정
# 동작 방식: 리스트를 순회하면서 인덱스를 사용해 특정 조건을 확인하고 항목을 제거

menu = ['삼겹살', '떡볶이', '덮밥', '삼겹살', '삼겹살', '삼겹살']
i = 0  # 초기 인덱스 설정
while i < len(menu):  # 인덱스가 리스트 길이보다 작을 때까지 반복
    if menu[i] == "삼겹살":  # 현재 항목이 '삼겹살'인지 확인
        menu.remove("삼겹살")  # 현재 인덱스의 '삼겹살' 제거
        # 리스트가 수정되면 나머지 항목이 앞으로 이동하여 인덱스가 자동 조정됨
        # '삼겹살'인 경우에는 인덱스를 증가시키면 안됨
    else:
        i += 1  # '삼겹살'이 아닌 경우에만 인덱스를 증가시킴

# 리스트를 끝까지 순회하면서 모든 '삼겹살' 제거

# 방법 3: 'for'와 리스트 복사본(menu[:])을 사용하여 리스트 수정
# 동작 방식: 리스트 복사본을 순회하면서 원본 리스트에서 특정 조건의 항목을 제거

menu = ['삼겹살', '떡볶이', '덮밥', '삼겹살', '삼겹살', '삼겹살']
for i in menu[:]:  # 리스트의 복사본(menu[:])을 순회
    if i == "삼겹살":  # 현재 항목이 '삼겹살'인지 확인
        menu.remove('삼겹살')  # 원본 리스트(menu)에서 해당 항목 제거

# 복사본을 순회하기 때문에 리스트 수정이 인덱스에 영향을 미치지 않음
# 리스트 복사본과 원본은 별개로 동작하여 안전하게 수정 가능

# 추가 방법: count 메소드를 이용
menu = ['삼겹살', '떡볶이', '덮밥', '삼겹살', '삼겹살', '삼겹살']
for i in range(menu.count("삼겹살")):
    menu.remove('삼겹살')  # 원본 리스트(menu)에서 해당 항목 제거

print("최종 메뉴 =", menu)  # 최종 결과 출력

